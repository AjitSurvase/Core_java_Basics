package Shyna_Tech_Yarns;

public class pattern {
    public static void main(String[] args) {

        for(int i=1;i<=5;i++)                       //lines
        {
            for(int j=1; j<=i; j++)                 //star
            {
                System.out.print("*");                 //* print or values
            }
            System.out.println();
        }


    }
}
